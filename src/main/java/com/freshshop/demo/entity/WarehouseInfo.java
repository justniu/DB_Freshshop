package com.freshshop.demo.entity;

public class WarehouseInfo {
    private String warehouseId;
    private String warehouseName;
    private int warehouseCapacity;
    private String warehouseAddressDetail;
	public String getWarehouseId() {
		return warehouseId;
	}
	public void setWarehouseId(String warehouseId) {
		this.warehouseId = warehouseId;
	}
	public String getWarehouseName() {
		return warehouseName;
	}
	public void setWarehouseName(String warehouseName) {
		this.warehouseName = warehouseName;
	}
	public int getWarehouseCapacity() {
		return warehouseCapacity;
	}
	public void setWarehouseCapacity(int warehouseCapacity) {
		this.warehouseCapacity = warehouseCapacity;
	}
	public String getWarehouseAddressDetail() {
		return warehouseAddressDetail;
	}
	public void setWarehouseAddressDetail(String warehouseAddressDetail) {
		this.warehouseAddressDetail = warehouseAddressDetail;
	}
    
    
}
