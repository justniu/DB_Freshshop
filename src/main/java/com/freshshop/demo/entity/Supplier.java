package com.freshshop.demo.entity;

public class Supplier {
    private String supplierId;
    private String supplierName;
    private int supplierProductClassId;
    private String supplierContactName;
    private String supplierTel;
    private String supplierEmail;
    private int supplierAddressId;
    private String supplierAddressDetail;
    private String supplierRemark;
	public String getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(String supplierId) {
		this.supplierId = supplierId;
	}
	public String getSupplierName() {
		return supplierName;
	}
	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}
	public int getSupplierProductClassId() {
		return supplierProductClassId;
	}
	public void setSupplierProductClassId(int supplierProductClassId) {
		this.supplierProductClassId = supplierProductClassId;
	}
	public String getSupplierContactName() {
		return supplierContactName;
	}
	public void setSupplierContactName(String supplierContactName) {
		this.supplierContactName = supplierContactName;
	}
	public String getSupplierTel() {
		return supplierTel;
	}
	public void setSupplierTel(String supplierTel) {
		this.supplierTel = supplierTel;
	}
	public String getSupplierEmail() {
		return supplierEmail;
	}
	public void setSupplierEmail(String supplierEmail) {
		this.supplierEmail = supplierEmail;
	}
	public int getSupplierAddressId() {
		return supplierAddressId;
	}
	public void setSupplierAddressId(int supplierAddressId) {
		this.supplierAddressId = supplierAddressId;
	}
	public String getSupplierAddressDetail() {
		return supplierAddressDetail;
	}
	public void setSupplierAddressDetail(String supplierAddressDetail) {
		this.supplierAddressDetail = supplierAddressDetail;
	}
	public String getSupplierRemark() {
		return supplierRemark;
	}
	public void setSupplierRemark(String supplierRemark) {
		this.supplierRemark = supplierRemark;
	}
    
    
}
